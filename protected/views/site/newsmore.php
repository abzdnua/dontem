                <p class="italic">На данный момент работы по реконструкции корпуса 7А ведутся с опережением графика.</p>
                <p>За пять месяцев с начала реконструкции генподрядчиком - ООО «Донбасстеплоэлектромонтаж» (ДОНТЭМ) - полностью выполнены работы по демонтажу старого электрофильтра, сборке и монтажу переходной рамы нового электрофильтра и опорного каркаса электрофильтра. На данный момент ведутся работы по монтажу зольных бункеров электрофильтра корпуса 7А. По состоянию на вторую декаду июля, степень выполнения этих работ составила около 50% - на данный момент, персонал ООО «ДОНТЭМ» работает с опережением планового графика реконструкции.</p>
                <p>К монтажу переходной рамы электрофильтра, весом около 300 тонн, сотрудники ООО «ДОНТЭМ» приступили в мае. Эта металлоконструкция является основой для монтажа оборудования электрофильтра. В течение мая-июня переходная рама электрофильтра корпуса 7А была полностью смонтирована, сварные соединения металлоконструкций постамента были выварены и затем обварены. Параллельно с монтажом переходной рамы персонал ООО «ДОНТЭМ» осуществлял укрупненную сборку зольных бункеров электрофильтра.</p>
                <p>Во второй декаде июня специалисты ООО «ДОНТЭМ» приступили к укрупненной сборке и монтажу опорного каркаса электрофильтра. Эти работы были закончены в первой декаде июля. После чего генподрядчик приступил к монтажу зольных бункеров электрофильтра: по состоянию на 15 июля смонтировано 16 зольных бункеров – один «полуфильтр».</p>
                <p>Все работы велись собственным персоналом компании ДОНТЭМ, для реализации этого проекта подрядчик приобрел башенный кран LINDEN COMANSA высотой 65 м, длиной стрелы 70 м и грузоподъемностью 18 тонн (в Украине подобных кранов – всего два).</p>
                <p class="strong">Полная замена электрофильтра энергоблока №7 Славянской ТЭС является основным объемом реконструкции корпуса 7А, металлоемкость данного проекта составляет более 1300 тонн.</p>
                <p>Напомним, что модернизация корпуса 7А, дополнительного оборудования и электрофильтров - первая очередь реконструкции <a href="">энергоблока №7 СлавТЭС</a>. Срок реализации всего проекта (4 очереди) - 34 месяца. В результате реконструкции срок эксплуатации основного технического оборудования будет продлен на 15 лет с улучшением основных технико-экономических показателей. В частности, будут увеличены электрическая нагрузка блока (до 800 МВт), его маневренность и диапазон регулирования нагрузки, снижены удельные расходы топлива и доведены до нормативных выбросы пыли, оксидов серы и азота.</p>
